/* Operadores logicos

&& -> And -> E / Todas as expressoes precisam ser verdadeiras para retornar true
|| -> OR -> Ou / Uma das expressoes precisam ser verdadeiras para retornar true
! -> NOT -> Não 
*/


if (10 && 12 > 5) {
    console.log('É maior');
}
else {
    console.log('É menor');
}

if (10 || 3 > 5) {
    console.log('É maior');
}
else {
    console.log('É menor');
}

console.log(!false) // True

